package java_Program;

public class NumberPattern1 {

	public static void main(String[] args) {

			for(int i=1;i<10;i++)
			{
				for(int j=1;j<=i;j++)
				{
					System.out.print(j);
				}
				
				System.out.println();
			}

			for(int k=8;k>1;k--)
			{
				for(int l=1;l<=k;l++)
				{
					System.out.print(l);
				}
				
				System.out.println();
			}
	}

}
